// Fallback function to get translation with English fallback
export const getTranslation = (lang: string, key: string, params?: Record<string, string | number>): string => {
  // Try exact match first
  let translation = translations[lang]?.[key];
  
  // If not found, try base language (e.g., "en" from "en-US")
  if (!translation) {
    const baseLang = lang.split("-")[0];
    translation = translations[baseLang]?.[key];
  }
  
  // Fallback to English
  if (!translation) {
    translation = translations["en-US"]?.[key] || key;
  }
  
  // Replace params
  if (params) {
    Object.entries(params).forEach(([paramKey, value]) => {
      translation = translation.replace(`{${paramKey}}`, String(value));
    });
  }
  
  return translation;
};
